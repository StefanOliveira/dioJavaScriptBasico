
// Funções declarativas
function mensagem() {
    console.log('Sou uma função!')
}
mensagem();

// Expressão de função
// nomeação
var funcao = function(){
    console.log('Sou uma mensagem da função de expressão!')
}
funcao();

// Arrow function
var funcao = () => {
    console.log('Sou uma arrow function!')
}
funcao()